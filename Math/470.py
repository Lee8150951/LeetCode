# The rand7() API is already defined for you.
# def rand7():
# @return a random integer in the range 1 to 7
class Solution:
  def rand10(self):
    """
    :rtype: int
    """
    while True:
      row = rand7()
      col = rand7()
      idx = (row - 1) * 7 + col
      if idx <= 40:
        return 1 + (idx - 1) % 10

if __name__=="__main__":
  method = Solution()
  answer = method.rand10()
  print(answer)